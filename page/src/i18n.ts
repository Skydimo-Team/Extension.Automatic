type MessageKey = keyof typeof bundles['en-US']
type SupportedLocale = keyof typeof bundles

const supportedLocales = Object.keys(bundles) as SupportedLocale[]
const DEFAULT_LOCALE: SupportedLocale = 'en-US'

function resolveLocale(): SupportedLocale {
  const injected = window.__SKYDIMO_EXT_PAGE__?.locale
  if (injected && supportedLocales.includes(injected as SupportedLocale)) return injected as SupportedLocale

  const preferred =
    typeof navigator !== 'undefined'
      ? [navigator.language, ...(navigator.languages ?? [])].filter(Boolean)
      : []

  for (const candidate of preferred) {
    if (supportedLocales.includes(candidate as SupportedLocale)) return candidate as SupportedLocale
    const base = candidate.split('-')[0]
    const match = supportedLocales.find((locale) => locale.split('-')[0] === base)
    if (match) return match
  }

  return DEFAULT_LOCALE
}

let locale: SupportedLocale = resolveLocale()
let currentMessages = bundles[locale] ?? bundles[DEFAULT_LOCALE]

const listeners = new Set<() => void>()

function emitChange() {
  for (const listener of listeners) listener()
}

function subscribe(listener: () => void) {
  listeners.add(listener)
  return () => listeners.delete(listener)
}

function getSnapshot() {
  return locale
}

export function t(key: MessageKey) {
  return currentMessages[key] ?? bundles[DEFAULT_LOCALE][key] ?? key
}

export function currentLocale() {
  return locale
}

export function setLocale(nextLocale: string) {
  if (!supportedLocales.includes(nextLocale as SupportedLocale) || nextLocale === locale) {
    return
  }

  locale = nextLocale as SupportedLocale
  currentMessages = bundles[nextLocale as SupportedLocale] ?? bundles[DEFAULT_LOCALE]
  emitChange()
}

export function useLocale() {
  return useSyncExternalStore(subscribe, getSnapshot)
}
